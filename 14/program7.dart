//do while and switch


void main() {
	
	int x = 1;

	do{
		print("1:c");
		print("2:cpp");
		print("3:java");
		print("4:dart");

		switch(x) {
		case 1:
			print("1972");
        case 2:
			print("1978");
	    case 3:
			print("1995");
		case 4:
			print("2013");
	    default:
		 	print("No Match");
		}
	}while(false);
}